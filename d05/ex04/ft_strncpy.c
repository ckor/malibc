char		*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int		i;

	i = 0;
	while (*(src + i) && (n > 0) && (*(dest + i) = *(src + i)))
	{
		i++;
		n--;
	}
	if (n > 0)
	  *(dest + i) = '\0';
	return (dest);
}
