char		*ft_strlowcase(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if ( !(i) || str[i - 1] == ' ')
		{
		  str[i] -= (str[i] >= 'a' && str[i] <= 'z') ? (' '): (0);
		}
		i++;
	}
	return (str);
}
